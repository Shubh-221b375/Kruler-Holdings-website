/**
 * Builds compressed WebP variants next to originals in public/media and writes generatedMediaVariants.js.
 * Skips when public/media is missing (CDN-only CI builds keep the committed manifest).
 */
import './load-root-env.mjs';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import sharp from 'sharp';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const mediaRoot = path.join(root, 'public', 'media');
const outFile = path.join(root, 'src', 'data', 'generatedMediaVariants.js');

const WIDTHS = [
  { key: 960, maxWidth: 960, quality: 80 },
  { key: 1280, maxWidth: 1280, quality: 82 },
];

const SOURCE_EXT = /\.(jpe?g|png)$/i;
const SKIP_IF_SMALLER_THAN = 90 * 1024;

function mediaPathKey(folderName, fileName) {
  return `/media/${folderName}/${fileName}`;
}

function walkImages(dir, folderRel, out) {
  if (!fs.existsSync(dir)) return;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ent.name.startsWith('.')) continue;
    const abs = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      walkImages(abs, path.join(folderRel, ent.name), out);
      continue;
    }
    if (!SOURCE_EXT.test(ent.name)) continue;
    if (/\.w\d+\.webp$/i.test(ent.name)) continue;
    out.push({ abs, folderRel, fileName: ent.name });
  }
}

async function ensureVariant({ abs, folderRel, fileName }, { key, maxWidth, quality }) {
  const folderName = folderRel.split(path.sep).join('/');
  const base = fileName.replace(/\.[^.]+$/i, '');
  const variantName = `${base}.w${key}.webp`;
  const variantAbs = path.join(path.dirname(abs), variantName);
  const srcStat = fs.statSync(abs);
  if (srcStat.size < SKIP_IF_SMALLER_THAN) return null;

  if (fs.existsSync(variantAbs)) {
    const vStat = fs.statSync(variantAbs);
    if (vStat.mtimeMs >= srcStat.mtimeMs) {
      return { key, path: mediaPathKey(folderName, variantName) };
    }
  }

  await sharp(abs)
    .rotate()
    .resize({ width: maxWidth, withoutEnlargement: true })
    .webp({ quality, effort: 4 })
    .toFile(variantAbs);

  return { key, path: mediaPathKey(folderName, variantName) };
}

if (!fs.existsSync(mediaRoot)) {
  console.log('[generate-media-variants] Skipped: no public/media');
  process.exit(0);
}

const files = [];
walkImages(mediaRoot, '', files);

/** @type {Record<string, Record<number, string>>} */
const manifest = {};
let written = 0;

for (const file of files) {
  const folderName = file.folderRel.split(path.sep).join('/');
  const originalKey = mediaPathKey(folderName, file.fileName);
  const variants = {};

  for (const spec of WIDTHS) {
    try {
      const result = await ensureVariant(file, spec);
      if (result) {
        variants[result.key] = result.path;
        written += 1;
      }
    } catch (err) {
      console.warn('[generate-media-variants] Skip', file.fileName, err.message);
    }
  }

  if (Object.keys(variants).length) manifest[originalKey] = variants;
}

const body =
  '/**\n' +
  ' * Auto-generated by npm run generate:media-variants — do not edit by hand.\n' +
  ' * Upload `*.w960.webp` / `*.w1280.webp` beside originals on S3 after running locally.\n' +
  ' */\n' +
  `export const MEDIA_VARIANTS = ${JSON.stringify(manifest, null, 2)};\n`;

fs.writeFileSync(outFile, body, 'utf8');
console.log(`[generate-media-variants] Wrote ${Object.keys(manifest).length} entries (${written} files) →`, path.relative(root, outFile));
