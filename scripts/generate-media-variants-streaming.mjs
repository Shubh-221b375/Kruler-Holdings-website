/**
 * Low-disk variant pipeline: fetch each JPG/PNG from CDN → WebP in memory → optional S3 upload.
 * Does NOT keep full originals on disk (unlike download + generate-media-variants).
 *
 *   npm run media:variants:deploy
 *
 * Needs ~200MB free (not gigabytes). Configure AWS for upload: aws configure
 */
import './load-root-env.mjs';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { spawnSync } from 'child_process';
import sharp from 'sharp';
import { generatedPropertyImages } from '../src/data/generatedPropertyImages.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const mediaRoot = path.join(root, 'public', 'media');
const outFile = path.join(root, 'src', 'data', 'generatedMediaVariants.js');
const cdnBase = (process.env.VITE_MEDIA_BASE_URL || 'https://kruler-holdings-website.s3.amazonaws.com').replace(
  /\/+$/,
  ''
);
const bucket = process.env.S3_BUCKET || 'kruler-holdings-website';
const SKIP_BYTES = 90 * 1024;

const WIDTHS = [
  { key: 960, maxWidth: 960, quality: 80 },
  { key: 1280, maxWidth: 1280, quality: 82 },
];

function hasAwsCli() {
  const r = spawnSync('aws', ['--version'], { stdio: 'pipe', shell: process.platform === 'win32' });
  return r.status === 0;
}

function mediaPathKey(folderName, fileName) {
  return `/media/${folderName}/${fileName}`;
}

function collectPaths() {
  const set = new Set();
  for (const list of Object.values(generatedPropertyImages)) {
    if (!Array.isArray(list)) continue;
    for (const p of list) {
      const s = String(p);
      if (!s.startsWith('/media/')) continue;
      if (!/\.(jpe?g|png)$/i.test(s)) continue;
      const decoded = decodeURIComponent(s);
      if (/\blogo\b/i.test(decoded)) continue;
      set.add(s);
    }
  }
  return [...set];
}

function parseMediaPath(encodedPath) {
  const rel = decodeURIComponent(encodedPath.replace(/^\/media\//, ''));
  const slash = rel.lastIndexOf('/');
  return {
    folder: rel.slice(0, slash),
    file: rel.slice(slash + 1),
    rel,
  };
}

function uploadToS3(localPath, s3Key) {
  const r = spawnSync(
    'aws',
    ['s3', 'cp', localPath, `s3://${bucket}/${s3Key}`, '--content-type', 'image/webp'],
    { stdio: 'pipe', shell: process.platform === 'win32' }
  );
  return r.status === 0;
}

const paths = collectPaths();
const upload = hasAwsCli() && process.env.SKIP_S3_UPLOAD !== '1';

console.log(`[streaming] ${paths.length} photos to process (CDN → WebP, minimal disk)`);
if (!upload) {
  console.warn('[streaming] AWS CLI missing or SKIP_S3_UPLOAD=1 — variants saved under public/media only.');
}

/** @type {Record<string, Record<number, string>>} */
const manifest = {};
let done = 0;
let skipped = 0;

for (const mediaPath of paths) {
  const url = cdnBase + mediaPath;
  const { folder, file, rel } = parseMediaPath(mediaPath);
  const originalKey = mediaPathKey(folder, file);
  const base = file.replace(/\.[^.]+$/i, '');

  let buf;
  try {
    const res = await fetch(url, { redirect: 'follow' });
    if (!res.ok) {
      skipped += 1;
      console.warn('[streaming] skip', res.status, file);
      continue;
    }
    buf = Buffer.from(await res.arrayBuffer());
  } catch (err) {
    skipped += 1;
    console.warn('[streaming] skip', file, err.message);
    continue;
  }

  if (buf.length < SKIP_BYTES) {
    skipped += 1;
    continue;
  }

  const variants = {};
  for (const spec of WIDTHS) {
    const variantName = `${base}.w${spec.key}.webp`;
    const variantRel = rel.replace(/[^/]+$/, variantName);
    const localPath = path.join(mediaRoot, variantRel);

    try {
      const webp = await sharp(buf)
        .rotate()
        .resize({ width: spec.maxWidth, withoutEnlargement: true })
        .webp({ quality: spec.quality, effort: 4 })
        .toBuffer();

      fs.mkdirSync(path.dirname(localPath), { recursive: true });
      fs.writeFileSync(localPath, webp);

      if (upload) {
        const s3Key = `media/${variantRel.replace(/\\/g, '/')}`;
        if (uploadToS3(localPath, s3Key)) {
          if (process.env.KEEP_LOCAL_VARIANTS !== '1') {
            try {
              fs.unlinkSync(localPath);
            } catch {
              /* ignore */
            }
          }
        } else {
          console.warn('[streaming] upload failed', variantName);
        }
      }

      variants[spec.key] = mediaPathKey(folder, variantName);
    } catch (err) {
      console.warn('[streaming] variant failed', variantName, err.message);
    }
  }

  if (Object.keys(variants).length) {
    manifest[originalKey] = variants;
    done += 1;
    if (done % 5 === 0) console.log(`[streaming] ${done}/${paths.length}…`);
  }

  buf = null;
}

const body =
  '/**\n' +
  ' * Auto-generated by npm run media:variants:deploy (streaming, low disk).\n' +
  ' * WebP files live on S3 beside originals.\n' +
  ' */\n' +
  `export const MEDIA_VARIANTS = ${JSON.stringify(manifest, null, 2)};\n`;

fs.writeFileSync(outFile, body, 'utf8');
console.log(`[streaming] Done: ${done} sources, ${skipped} skipped → ${path.relative(root, outFile)}`);
if (upload) {
  console.log('[streaming] WebP variants uploaded to s3://' + bucket + '/media/');
} else {
  console.log('[streaming] Commit generatedMediaVariants.js after uploading variants to S3.');
}
